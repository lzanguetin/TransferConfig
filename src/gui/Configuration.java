    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        Config conf = new Config();

        conf.setFlag(jTextField1.getText());
        conf.setQtd(jTextField2.getText());
        conf.setMenu(jTextField3.getText());

        FileWriter arq = null;

        try {
            arq = new FileWriter("C:\\BotsFolder\\transferbot.ini");
        }catch (IOException ex) {
            Logger.getLogger(Configuration.class.getName()).log(Level.SEVERE, null, ex);
        }
        PrintWriter gravarArq = new PrintWriter(arq);

        gravarArq.printf("id.menu.flag=%s\n", conf.getFlag());
        gravarArq.printf("id.qt.opt=%s\n", conf.getQtd());
        gravarArq.printf("id.menu.config=%s\n", conf.getMenu());

        try {
            arq.close();
        } catch (IOException ex) {
            Logger.getLogger(Configuration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jLabel5.setText("Sucesso!");
    }

