package modelo;

public class Config {
    String flag;
    String qtd;
    String menu;

    public String getFlag() {
        return flag;
    }
        
    public void setFlag(String flag) {
        this.flag = flag;
    }

    public String getQtd() {
        return qtd;
    }
    
    public void setQtd(String qtd) {
        this.qtd = qtd;
    }

    public String getMenu() {
        return menu;
    }
        
    public void setMenu(String menu) {
        this.menu = menu;
    }
}
